-- FIND THE UNIQUE VALUES IN GENDER COLUMN

SELECT GENDER, count(*) FROM TITANIC group by GENDER;

SELECT distinct GENDER FROM TITANIC;

-- FIND THE SURVIVAL COUNT OF MALE AND FEMAIL

SELECT GENDER, COUNT(*) AS TOTAL FROM TITANIC
WHERE SURVIVED = 1
GROUP BY GENDER;

-- FIND SURVIVED AND NON SURVIVED GENDER COUNT
SELECT GENDER, SURVIVED, COUNT(*) FROM TITANIC
group by GENDER, SURVIVED;

SELECT GENDER, CASE
			   WHEN SURVIVED=1 THEN "SURVIVED"
               WHEN SURVIVED=0 THEN "NON-SURVIVED" END AS SURVIVED,
               COUNT(*) FROM TITANIC
GROUP BY GENDER, SURVIVED;

-- FIND EACH CLASS TOTAL PASSENEGER IN DESCENDING ORDER
SELECT PCLASS, COUNT(*) TOTAL_COUNT FROM TITANIC
group by PCLASS
order by TOTAL_COUNT DESC;

-- FIND TOP 5 TICKET PRICE RECORDS
SELECT FARE FROM TITANIC
ORDER BY FARE DESC
LIMIT 5;

-- FIND SECOND HIGHEST TICKET PRICE
SELECT DISTINCT FARE FROM TITANIC
ORDER BY FARE DESC
LIMIT 1 OFFSET 1;


-- RETURN PCLASS WHERE TOTAL PASSENGERS COUNT IS MORE THAN 250
SELECT PCLASS , COUNT(*) FROM TITANIC
GROUP BY PCLASS
HAVING COUNT(*) > 250;


